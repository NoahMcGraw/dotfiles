#!/usr/bin/env bash

# git-shortcuts.bash
#
# A collection of git shortcuts for bash.

alias gs="git status"
alias gch="git checkout"
alias ga="git add -A"
alias gaco="git add -A && git commit -m"
alias gco="git commit -m"
alias gpi="git pull"
alias gpo="git push"
alias gd="git diff"
alias gr="git rebase"
alias gm="git merge"
